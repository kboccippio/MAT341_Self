function lcm_val = calculate_LCM(x,y)

%for loop for multiples of x
for i = 1:y
   x1 = x * i;

   %for loop for multiples of y
    for j = 1:x
        y1 = y * j;

        %if statement to determine whether multiples are equal
        if x1 == y1
                
           %print statement
           "the lcm of " + x + " and " + y + " is " + x1
           
            %exit all loops
            return
        end
    end
end
